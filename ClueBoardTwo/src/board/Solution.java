package board;

public class Solution {

	String person;
	String weapon;
	String room;
	
	public Solution() {
		
	}
	
	public Solution( String person, String weapon, String room ) {
		this.person = person;
		this.weapon = weapon;
		this.room = weapon;
	}

	public String getPerson() {
		return person;
	}

	public String getWeapon() {
		return weapon;
	}

	public String getRoom() {
		return room;
	}
	
	
}
